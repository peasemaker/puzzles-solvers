export interface Level {
    signs: string,
    fields: string
}

function generateStandardFields(): string {
    let fields = '';

    for (let x = 0; x < 9; x++) {
        for (let y = 0; y < 9; y++) {
            fields += Math.floor(x / 3) * 3 + Math.floor(y / 3) + 1 ;
        }
        fields += '\n';
    }

    return fields;
}

